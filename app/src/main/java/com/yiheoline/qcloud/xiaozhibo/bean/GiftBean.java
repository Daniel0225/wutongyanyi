package com.yiheoline.qcloud.xiaozhibo.bean;

public class GiftBean {
    private String createTime;
    private String giftLogo;
    private int id;
    private String name;
    private int wutongye;

    public String getCreateTime() {
        return createTime;
    }

    public void setCreateTime(String createTime) {
        this.createTime = createTime;
    }

    public String getGiftLogo() {
        return giftLogo;
    }

    public void setGiftLogo(String giftLogo) {
        this.giftLogo = giftLogo;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getWutongye() {
        return wutongye;
    }

    public void setWutongye(int wutongye) {
        this.wutongye = wutongye;
    }
}
